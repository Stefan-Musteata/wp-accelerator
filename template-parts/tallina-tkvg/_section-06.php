	<h3 class="h5 mt-4 mb-2">6.3 Managementul riscurilor și viziunea 2026–2030</h3>
	<p>Pentru a răspunde provocărilor identificate, TKVG a formulat direcții strategice clare, interconectate, reflectând o viziune holistică asupra transformării educaționale.</p>
	<div class="tkvg-strategic">
		<article>
			<h4 class="h6">Cadrul etic pentru utilizarea inteligenței artificiale</h4>
			<p class="mb-0">Primul obiectiv vizează dezvoltarea unui cadru etic pentru utilizarea inteligenței artificiale, marcând trecerea de la faza de experimentare la una de responsabilitate instituțională. Accentul se mută de la utilizare spontană la reglementare conștientă și practici asumate.</p>
		</article>
		<article>
			<h4 class="h6">Personalizarea procesului educațional prin analiza datelor</h4>
			<p class="mb-0">Al doilea obiectiv este aprofundarea personalizării procesului educațional prin utilizarea analizei de date, cu scopul de a reduce supraîncărcarea elevilor, în special în „zona de criză” identificată. Direcția urmărește optimizarea echilibrului între performanță și sustenabilitate cognitivă.</p>
		</article>
		<article>
			<h4 class="h6">Sustenabilitate și stare de bine</h4>
			<p class="mb-0">Accentul pus pe sustenabilitate și stare de bine reflectă recunoașterea faptului că succesul tehnologic pe termen lung este inseparabil de sănătatea emoțională și profesională a elevilor și profesorilor. Transformarea digitală este concepută ca un proces echilibrat, centrat pe oameni.</p>
		</article>
	</div>
