    <h3 class="h5 mt-4 mb-2">1.3 Istoricul digital (punctul de plecare)</h3>
            <p>Momentul „Digipööre” (2016): transformarea digitală sistemică a început în 2016, prin participarea la proiectul „Digital Turn” (Digipööre). Acesta a marcat trecerea de la simpla utilizare a tehnologiei la o schimbare culturală profundă.</p>
