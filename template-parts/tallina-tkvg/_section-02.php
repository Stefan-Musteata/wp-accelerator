            <p>Graficul radar DigiPeegel evidențiază un profil dezechilibrat al maturității digitale instituționale în anul 2017, cu un nivel relativ mai avansat al infrastructurii digitale comparativ cu dimensiunile pedagogice și de leadership. Acest rezultat a fundamentat necesitatea unei transformări digitale sistemice, orientate spre dezvoltarea competențelor profesionale și a guvernanței educaționale, nu doar spre dotare tehnologică.</p>

