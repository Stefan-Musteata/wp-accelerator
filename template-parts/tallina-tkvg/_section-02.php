	<div class="tkvg-divider">2. Traiectoria transformării digitale</div>
	<p>Transformarea digitală la Tallinna Kesklinna Vene Gümnaasium (TKVG) a reprezentat un proces instituțional progresiv, desfășurat pe o peste un deceniu, și nu o intervenție punctuală. Evoluția a urmărit o tranziție strategică de la digitalizarea infrastructurală către dezvoltarea competențelor pedagogice ale cadrelor didactice, culminând cu personalizarea învățării prin utilizarea inteligenței artificiale, centrată pe nevoile elevilor.</p>

