	<h4 class="h6 mt-3 mb-2">4.4 Exemple de învățare dublă (double-loop learning)</h4>
	<p>TKVG aplică principiul „organizației care învață” prin revizuirea sistematică a propriilor procese:</p>
	<ol>
		<li>Revizuirea anuală a strategiei: în fiecare lună august, Consiliul Profesoral analizează rezultatele evaluării interne din anul precedent pentru a redefini obiectivele noului an școlar.</li>
		<li>Corecția bazată pe fapte: strategia digitală nu este statică; ea este corectată anual prin integrarea feedback-ului din mediile de învățare e-learning și din sondajele SELFIE, asigurând astfel că investițiile în tehnologie răspund nevoilor reale, nu doar unor dorințe tehnice.</li>
	</ol>

