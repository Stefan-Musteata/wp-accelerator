			<p class="text-body text-justify lh-base mb-3">
				Schimbarea a fost impulsionată de factori interni și externi care au forțat instituția să evolueze:
			</p>
			<ul class="text-body text-justify lh-base mb-4 ps-3">
				<li class="mb-2">
					<strong>Competiția pentru excelență:</strong> Dorința de a obține statutul de „școală a secolului XXI” și de a face parte din Uniunea G5 (elitele școlilor din Tallinn) a funcționat ca un motor constant pentru inovare.
				</li>
				<li class="mb-2">
					<strong>Eficiența administrativă:</strong> Nevoia de a reduce sarcinile birocratice ale profesorilor prin automatizare și sisteme „paperless”.
				</li>
				<li class="mb-2">
					<strong>Presiunea beneficiarilor:</strong> Așteptările părinților de a vedea competențe digitale reale integrate în curriculum, pregătind elevii pentru piața muncii.
				</li>
				<li class="mb-0">
					<strong>Pandemia COVID-19:</strong> A reprezentat un test de stres care a forțat utilizarea intensivă a platformelor precum Microsoft Teams și a validat investițiile anterioare în infrastructură, asigurând o trecere echilibrată la învățământul la distanță.
				</li>
			</ul>
			<p class="text-body text-justify lh-base mb-5">
				<strong>Mandate naționale și evoluții tehnologice:</strong> Introducerea examenelor electronice obligatorii începând cu 2025 și ascensiunea inteligenței artificiale au acționat ca declanșatori recenți pentru programe precum <strong>AI Leap</strong>.
			</p>

