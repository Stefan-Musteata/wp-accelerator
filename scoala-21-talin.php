			<p class="text-body text-justify lh-base mb-4">
				<strong>Managementul Executiv și Autonomia. Conducerea</strong> este asigurată de director, care, în conformitate cu modelul estonian, deține drepturi extinse în organizarea vieții școlare. Directorul are autoritatea de a gestiona bugetul, de a stabili salariile profesorilor și de a adapta oferta educațională — până la <strong>25% din acesta poate fi dezvoltat la nivel de școală</strong>, permițând introducerea unor materii precum Robotica în trunchiul obligatoriu.
			</p>

