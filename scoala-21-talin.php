			</p>

			<h3 class="h5 fw-semibold mb-3">2.1 Etape-cheie și momente de cotitură</h3>
			<p class="text-body text-justify lh-base mb-3">
				Transformarea digitală a școlii poate fi segmentată în mai multe etape definitorii:
			</p>
			<ul class="text-body text-justify lh-base mb-0 ps-3">
				<li class="mb-2">
					<strong>Programul Tiger Leap (1996–1997):</strong> Punctul de pornire a fost marcat de inițiativa națională <em>Tiigrihüpe</em>, prin care școala a fost dotată cu prima sa sală de calculatoare în 1997 și conectată la Internet.
				</li>
				<li class="mb-2">
					<strong>Modernizarea Structurală (2001–2003):</strong> Renovarea capitală a clădirii istorice a reprezentat un moment de cotitură tehnic, fiind concepută special pentru a susține o infrastructură IT robustă, sisteme multimedia în fiecare clasă și <strong>conectivitate wireless totală</strong> în tot campusul.
				</li>
				<li class="mb-2">
					<strong>Digitalizarea Administrativă (2006):</strong> Școala a realizat tranziția completă către sistemul <strong>eKool</strong> (catalogul digital), moment care a eliminat birocrația pe hârtie și a transformat comunicarea cu părinții într-un proces în timp real.
				</li>
				<li class="mb-2">
					<strong>Era STEAM și Inovației (2018):</strong> Lansarea studioului <strong>EduInnoLab</strong>, un spațiu dotat cu robotică, imprimante 3D și echipamente VR, a marcat maturizarea componentelor de creație digitală. Proiectul a fost recunoscut prin premiul național „Education Act 2018”.
				</li>
				<li class="mb-0">
					<strong>Integrarea Curriculară Avansată (2022–2024):</strong> Lansarea unor profiluri specializate precum „Muzică și Tehnologie Digitală” (2022) și „Științe Reale și Infotehnologie” (2024) a demonstrat că digitalul a devenit parte integrantă a identității educaționale, nu doar un instrument suport.
				</li>
			</ul>
