					<strong>Consiliul de Guvernanță:</strong> Este un organ strategic de supraveghere format din părinți, profesori, elevi, absolvenți și reprezentanți ai autorității municipale (Primăria Tallinn, care este proprietarul școlii). Acesta decide direcțiile majore de dezvoltare.
