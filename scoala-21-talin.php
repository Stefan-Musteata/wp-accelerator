			<p class="text-body text-justify lh-base mb-3">
				Învățarea cu buclă dublă presupune nu doar corectarea unei erori, ci <strong>chestionarea ipotezelor și valorilor fundamentale</strong> care stau la baza unei practici.
			</p>
			<ul class="text-body text-justify lh-base mb-4 ps-3">
				<li class="mb-2">
					<strong>Cazul „Hardware vs. Pedagogie”:</strong> Când datele au arătat că utilizarea tabletelor la orele de matematică nu îmbunătățea notele, conducerea nu a optat pentru tablete mai scumpe (buclă simplă). În schimb, au identificat că ipoteza de bază — predarea tradițională pe un suport digital — era greșită. <strong>Decizia de buclă dublă:</strong> Reproiectarea completă a procesului prin trecerea la modelul <strong>STEAM</strong>, unde tableta a devenit un instrument de creație, nu de vizualizare.
				</li>
				<li class="mb-2">
					<strong>Gestionarea Inteligenței Artificiale:</strong> Observând tentația ridicată de plagiat prin ChatGPT, școala nu a ales interzicerea tehnologiei (buclă simplă). <strong>Decizia de buclă dublă:</strong> Chestionarea modului de evaluare. Școala a introdus cursuri de <strong>„Alfabetizare AI”</strong> și a schimbat radical criteriile de notare: procesul de cercetare documentat și declararea surselor AI contează acum la fel de mult ca rezultatul final.
				</li>
				<li class="mb-2">
					<strong>Revizuirea Scaffolding-ului în Oracy:</strong> Dovezile colectate de programul <strong>Voice 21</strong> au arătat că anumite tehnici de dezbatere nu erau accesibile elevilor din medii defavorizate. În loc să simplifice materia, școala a reevaluat curriculum-ul și a creat structuri de sprijin lingvistic (<strong>scaffolding</strong>) mult mai robuste pentru a asigura echitatea.
				</li>
				<li class="mb-0">
					<strong>Protocolul de „Tuning” între colegi:</strong> Profesorii folosesc protocoale unde își prezintă planurile de lecție și primesc critici constructive de la colegi. Această practică îi obligă să își regândească fundamentele pedagogice ori de câte ori datele privind implicarea elevilor sunt scăzute, transformând o problemă de moment într-o reformă a metodei de predare.
				</li>
			</ul>
			<p class="text-body text-justify lh-base mb-0">
				Prin aceste mecanisme, Școala 21 demonstrează că transformarea digitală reală este un proces continuu de adaptare, unde datele sunt „busola” care asigură că tehnologia servește întotdeauna pedagogia și progresul real al fiecărui elev.
