			<p class="text-body text-justify lh-base mb-3">
				Sistemul decizional al Școlii 21 este <strong>descentralizat</strong> și bazat pe dovezi, punând un accent major pe autoreglarea instituțională:
			</p>
			<ul class="text-body text-justify lh-base mb-5 ps-3">
				<li class="mb-2">
					<strong>Autonomia curriculară:</strong> Școala are libertatea de a decide asupra a <strong>25% din propriul curriculum</strong>. Această libertate a permis introducerea <strong>roboticii ca materie obligatorie</strong> pentru ciclul primar, deși nu este o cerință la nivel național, și crearea unor profiluri unice precum „Muzică și Tehnologie Digitală”.
				</li>
				<li class="mb-2">
					<strong>Managementul bazat pe date (Digital Mirror):</strong> Deciziile strategice nu sunt luate intuitiv, ci pe baza instrumentului național <strong>Digital Mirror</strong>. Școala colectează date pe 400 de puncte de analiză pentru a-și evalua maturitatea digitală și pentru a stabili planuri de îmbunătățire de tip SMART.
				</li>
				<li class="mb-2">
					<strong>Cultura feedback-ului și învățarea cu buclă dublă:</strong> Școala utilizează datele colectate (sondaje de satisfacție, analitica platformei Opiq) pentru a-și chestiona paradigmele educaționale. Un exemplu este decizia de a <strong>reproiecta procesul de învățare</strong> în loc de a cumpăra hardware nou atunci când datele au arătat că utilizarea tabletelor fără o schimbare pedagogică nu îmbunătățea rezultatele.
				</li>
				<li class="mb-0">
					<strong>Colaborarea inter-instituțională:</strong> Deciziile privind inovația sunt alimentate și de participarea lunară la întâlniri cu liderii IT din alte școli, facilitând schimbul de bune practici.
				</li>
			</ul>

			<h3 class="h5 fw-semibold mb-3">3.3 Nivelul de participare al cadrelor didactice</h3>
			<p class="text-body text-justify lh-base mb-3">
				Profesorii de la Școala 21 beneficiază de un nivel de <strong>autonomie profesională printre cele mai ridicate din lume</strong>, fiind considerați „designeri ai mediilor de învățare”.
			</p>
			<ul class="text-body text-justify lh-base mb-4 ps-3">
				<li class="mb-2">
					<strong>Libertate pedagogică totală:</strong> Cadrele didactice au responsabilitatea principală în <strong>selectarea metodelor de predare, a manualelor digitale și a instrumentelor de evaluare</strong>. Această autonomie le permite să experimenteze formate inovatoare, precum <strong>Flipped Classroom</strong> sau utilizarea inteligenței artificiale ca asistent în cercetare.
				</li>
				<li class="mb-2">
					<strong>Comunități de Învățare Profesională (PLC):</strong> Profesorii sunt organizați pe departamente/catedre și participă la grupuri de reflecție unde fac schimb de experiență și colaborează la designul proiectelor interdisciplinare. Aceștia folosesc protocoale de tip „Tuning Protocol” pentru a primi feedback critic de la colegi asupra planurilor de lecție.
				</li>
				<li class="mb-2">
					<strong>Angajamentul față de formarea continuă:</strong> Școala alocă legal <strong>1% din bugetul de salarii</strong> pentru dezvoltarea profesională continuă (CPD). Profesorii sunt încurajați să devină lideri de schimbare, participând activ la implementarea programelor naționale, cum este actualul <strong>AI Leap (2025)</strong>, pentru care școala a elaborat ghiduri proprii de utilizare etică a AI.
				</li>
				<li class="mb-0">
					<strong>Responsabilitatea pentru performanță:</strong> Autonomia este echilibrată de un sistem de monitorizare riguros. Progresul fiecărui profesor este urmărit prin planuri de învățare personală (PLP), care includ obiective de atenuare a subperformanței elevilor și de integrare a noilor tehnologii.
				</li>
			</ul>
			<p class="text-body text-justify lh-base mb-0">
				În concluzie, leadership-ul la Școala 21 este unul <strong>distribuit și participativ</strong>, unde succesul este garantat de un pact de încredere între management și profesori, susținut de date concrete și de o cultură care valorizează inovația fără a sacrifica rigoarea.
			</p>
