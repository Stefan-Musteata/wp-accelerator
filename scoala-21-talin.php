			<p class="text-body text-justify lh-base mb-3">
				Transformarea digitală a Școlii 21 este un proces de lungă durată, cu un orizont temporal care se întinde pe patru decenii:
			</p>
			<ul class="text-body text-justify lh-base mb-4 ps-3">
				<li class="mb-2">
					<strong>Trecutul (1996–2011):</strong> Faza de alfabetizare digitală de bază și creare a infrastructurii critice.
				</li>
				<li class="mb-2">
					<strong>Prezentul (2012–2025):</strong> Faza de transformare pedagogică profundă, marcată de trecerea de la utilizarea „accidentală” a tehnologiei la <strong>integrarea invizibilă</strong> în rutina zilnică (rutină pedagogică).
				</li>
				<li class="mb-0">
					<strong>Viitorul (2025–2035):</strong> Alinierea cu „Strategia Educației 2035”, care vizează personalizarea profundă a învățării prin AI, utilizarea analiticii de date pentru optimizarea progresului elevilor și implementarea completă a evaluărilor digitale adaptive.
				</li>
			</ul>
			<p class="text-body text-justify lh-base mb-0">
				În concluzie, pentru Școala 21, digitalizarea nu este o destinație finală, ci o <strong>călătorie continuă de adaptare</strong>, unde orizontul temporal se recalibrează constant în funcție de noile salturi tehnologice, cum este actualul „AI Leap” din 2025.
