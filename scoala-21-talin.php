			<p class="text-body text-justify lh-base mb-4">
				Tallinna 21. Kool este o instituție de mari dimensiuni, raportând pentru anul școlar 2025/2026 un efectiv de <strong>1387 de elevi</strong> și un corp profesoral format din <strong>95 de cadre didactice</strong>. Acești elevi sunt organizați în <strong>45 de clase</strong>, indicând un grad ridicat de complexitate organizațională. Infrastructura digitală este impresionantă: <strong>430 de laptopuri, 85 de iPad-uri, 50 de tablete Android și 35 de ecrane inteligente și 60 de proiectoare.</strong>
