				Modelul de leadership și guvernanță al <strong>Școlii 21 din Tallinn (Tallinna 21. Kool)</strong> este un exemplu de referință pentru sistemul educațional estonian, caracterizându-se printr-o <strong>autonomie instituțională extinsă</strong> și o structură organizatorică complexă, menită să susțină inovația pedagogică și excelența academică. Guvernanța instituției nu este doar o structură administrativă, ci un mecanism agil care îmbină <strong>managementul executiv puternic</strong> cu <strong>organisme de decizie colective</strong> și participarea activă a tuturor actorilor educaționali.
			</p>

			<h3 class="h5 fw-semibold mb-3">3.1 Rolurile actorilor-cheie</h3>
			<p class="text-body text-justify lh-base mb-3">
				În cadrul Școlii 21, succesul transformării digitale și educaționale depinde de o distribuție clară și strategică a responsabilităților:
			</p>
			<ul class="text-body text-justify lh-base mb-0 ps-3">
				<li class="mb-3">
					<strong>Directorul școlii:</strong> Acesta ocupă o poziție centrală, beneficiind de <strong>drepturi extinse</strong> specifice modelului estonian, inclusiv autoritatea de a gestiona bugetul, de a stabili salariile profesorilor și de a decide angajarea specialiștilor. Directorul acționează ca un <strong>facilitator al schimbării</strong>, orientând viziunea strategică spre competențele secolului XXI.
				</li>
				<li class="mb-3">
					<strong>Departamentul IT și Tehnologii Educaționali:</strong> Guvernanța digitală este delegată unei echipe specializate de patru membri, cu roluri care separă suportul tehnic de cel pedagogic. <strong>Manager IT</strong> coordonează viziunea tehnică și securitatea cibernetică, în timp ce <strong>Specialist IT</strong> gestionează incidentele tehnice fizice. Un rol critic îl joacă cei doi <strong>tehnologi educaționali</strong>, descriși ca „oameni de legătură” care instruiesc profesorii și elevii în utilizarea platformelor digitale precum Teams, Opiq sau AI. Aceștia acționează ca mentori, reducând rezistența la schimbare și asigurând integrarea tehnologiei în pedagogie.
				</li>
				<li class="mb-3">
					<strong>Organismele colective de decizie:</strong> <strong>Consiliul de Guvernanță</strong> reprezintă forumul strategic unde părinții, profesorii, elevii și autoritățile locale supraveghează direcția școlii. În paralel, <strong>Consiliul Pedagogic</strong> este responsabil pentru deciziile didactice și coordonarea curriculum-ului.
				</li>
				<li class="mb-0">
					<strong>Elevii ca parteneri în leadership:</strong> Participarea acestora este formalizată prin <strong>Consiliul Elevilor</strong>, care oferă feedback constant prin chestionare instituționalizate. De asemenea, există o <strong>Echipă Tehnică (TechTeam)</strong> formată din elevi care asistă departamentul IT în gestionarea evenimentelor live și a transmisiunilor video.
				</li>
			</ul>
