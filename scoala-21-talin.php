			<p class="text-body text-justify lh-base mb-3">
				Dovezile colectate nu rămân la stadiul de statistici, ci devin motoare pentru schimbări concrete în viața școlii:
			</p>
			<ul class="text-body text-justify lh-base mb-4 ps-3">
				<li class="mb-2">
					<strong>Reproiectarea proceselor pedagogice (Cazul Tabletelo):</strong> Un exemplu definitoriu de utilizare a datelor în Școala 21 a fost momentul în care analizele au arătat că utilizarea tabletelor la orele de matematică nu aducea o îmbunătățire a notelor. În loc să achiziționeze hardware nou, conducerea a identificat că problema era pedagogia tradițională aplicată pe suport digital. Decizia a fost <strong>reproiectarea procesului de învățare</strong>, trecându-se la modelul STEAM, unde dispozitivele au devenit instrumente de creație, nu doar de vizualizare.
				</li>
				<li class="mb-2">
					<strong>Adaptarea la Inteligența Artificială:</strong> Observând tentația de plagiat indicată de datele calitative, școala nu a interzis AI-ul, ci a introdus cursuri de <strong>alfabetizare AI</strong>. Decizia a implicat schimbarea modului de evaluare a lucrărilor de cercetare: acum, procesul și declararea surselor AI contează la fel de mult ca produsul final.
				</li>
				<li class="mb-2">
					<strong>Individualizarea și Diferențierea:</strong> Datele din platformele adaptive permit profesorilor să decidă în timp real când un elev are nevoie de o sarcină mai complexă sau de sprijin suplimentar, evitând „plafonarea” elevilor dotați și abandonul celor care au lacune.
				</li>
				<li class="mb-2">
					<strong>Investiții strategice în infrastructură:</strong> Decizia de a instala dulapuri de încărcare pe fiecare etaj și de a asigura un set de peste 430 de laptopuri a fost bazată pe datele privind nevoia de mobilitate și frecvența utilizării tehnologiei în cadrul tuturor materiilor, nu doar la informatică.
				</li>
				<li class="mb-0">
					<strong>Monitorizarea Bunăstării (Wellbeing):</strong> Datele din sondajele privind bullying-ul permit echipei de suport (psihologi, pedagogi sociali) să intervină preventiv în colectivele de elevi unde tensiunile sunt în creștere, înainte ca acestea să afecteze rezultatele academice.
				</li>
			</ul>
			<p class="text-body text-justify lh-base mb-0">
				În concluzie, la Școala 21, datele sunt privite ca o <strong>„oglindă” instituțională</strong>. Ele forțează organizația să evolueze constant, asigurându-se că tehnologia servește pedagogia și că fiecare decizie administrativă are la bază dovezi concrete ale succesului elevului.
