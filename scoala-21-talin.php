				</li>
				<li class="mb-2">
					<strong>Integrarea Transversală (Digital English &amp; Digital Maths):</strong> Tehnologia este utilizată pentru a potența procesul educațional. În cadrul lecțiilor de <strong>Digital English</strong>, elevii folosesc platforma <strong>MS Teams</strong> pentru proiecte de colaborare internațională și resurse multimedia pentru imersiune culturală.
				</li>
				<li class="mb-2">
					La matematică și fizică, elevii utilizează simulatoare precum <strong>Tinkercad Circuits</strong> pentru a experimenta cu circuite electrice înainte de a lucra cu dispozitive fizice (Arduino).
				</li>
				<li class="mb-2">
					<strong>Muzica și Tehnologia Digitală:</strong> Elevii de gimnaziu utilizează software-uri profesionale de editare audio pentru a înțelege bazele <strong>muzicii electronice</strong>, combinând teoria muzicală cu abilitățile tehnice de producție.
				</li>
				<li class="mb-2">
					<strong>Proiectul de Inginerie „Rakett 21”:</strong> Aceasta este o competiție internă de anvergură, unde elevii aplică cunoștințe din fizică și tehnologie pentru a rezolva probleme complexe, utilizând programe precum <strong>FTOOL</strong> pentru calcule de rezistență a materialelor.
				</li>
				<li class="mb-2">
					<strong>Tastarea oarbă (10-finger typing):</strong> Școala pune un accent deosebit pe cursul de <strong>Keyboard Pro</strong>, considerând abilitatea de a tasta rapid și corect o competență esențială pentru succesul academic și profesional viitor.
				</li>
