			<h3 class="h5 fw-semibold mb-3">5.2 Rolul profesorului și al elevului</h3>
			<p class="text-body text-justify lh-base mb-3">
				Transformarea digitală a reconfigurat radical dinamica responsabilității în sala de clasă:
			</p>
			<ul class="text-body text-justify lh-base mb-5 ps-3">
				<li class="mb-2">
					<strong>Profesorul este mentor și facilitator.</strong> Profesorul a încetat să mai fie singura sursă de informație („the oracle”). La școala 21, profesorul acționează ca un <strong>designer al mediului de învățare</strong>. El gestionează spațiul de colaborare în <strong>Class Notebook</strong> (Teams), oferind feedback asincron și personalizat. Autonomia le permite profesorilor să selecteze singuri manualele digitale (platforma <strong>Opiq</strong>) și să experimenteze metode precum <strong>Flipped Classroom</strong>. Un suport esențial pentru profesori este oferit de <strong>tehnologii educaționali</strong>, care preiau sarcina instruirii pedagogice pe zona digitală, permițând cadrului didactic să se concentreze pe conținut.
				</li>
				<li class="mb-2">
					<strong>Elevul este „independent learner” și creator.</strong> Elevul devine un participant activ care își asumă responsabilitatea pentru propriul progres. Elevii își gestionează portofoliile digitale și învață să folosească feedback-ul oferit de platformele adaptive pentru a progresa. Prin învățarea bazată pe proiecte (PBL), elevii produc produse creative, precum filme, aplicații sau obiecte modelate 3D, care sunt prezentate public.
				</li>
				<li class="mb-0">
					<strong>Autoreglarea și disciplina:</strong> Absența soneriilor în școală este un simbol al responsabilizării elevului. Tranzițiile între ore se bazează pe respectarea orarului digital și pe autoreglare, promovând o cultură a respectului reciproc și a managementului timpului.
				</li>
			</ul>

			<h3 class="h5 fw-semibold mb-3">5.3 Coerența cu strategia școlii</h3>
			<p class="text-body text-justify lh-base mb-3">
				Toate aceste schimbări pedagogice sunt strâns aliniate cu viziunea strategică pe termen lung a instituției (<strong>Arengukava 2022–2027</strong>) și cu standardele naționale estoniene:
			</p>
			<ul class="text-body text-justify lh-base mb-4 ps-3">
				<li class="mb-2">
					<strong>Alinierea cu Competențele Secolului XXI:</strong> Practicile de la clasă sunt proiectate pentru a dezvolta competențe transversale: gândire critică, colaborare, creativitate și alfabetizare digitală. Strategia școlii nu vizează doar dotarea cu hardware, ci transformarea pedagogică profundă.
				</li>
				<li class="mb-2">
					<strong>Învățarea bazată pe Double-Loop Learning:</strong> Deciziile pedagogice sunt luate prin analiza dovezilor colectate prin instrumentul <strong>Digital Mirror</strong>. Un exemplu de coerență strategică este momentul în care datele au arătat că tabletele nu îmbunătățeau notele la matematică; în loc să cumpere dispozitive noi, școala a ales reproiectarea procesului de învățare, trecând de la vizualizarea digitală la modelul de creație <strong>STEAM</strong>.
				</li>
				<li class="mb-2">
					<strong>Pregătirea pentru examenele electronice:</strong> Implementarea constantă a testelor e-diagnostice și a evaluărilor pe platforme digitale este o etapă pregătitoare coerentă cu mandatul național al Estoniei de a trece complet la examene de stat electronice începând cu 2025.
				</li>
				<li class="mb-0">
					<strong>Cultura inovației și excelenței:</strong> Școala 21 face parte din grupul de elită G5, iar succesul elevilor la competiții de securitate cibernetică (Cybering) sau AI validează coerența dintre curriculumul implementat și obiectivele de înaltă performanță ale școlii.
				</li>
			</ul>
			<p class="text-body text-justify lh-base mb-0">
				În concluzie, pedagogia Școlii 21 este definită de o <strong>simbioză între autonomie și rigurozitate.</strong> Tehnologia nu este un scop în sine, ci un vehicul prin care profesorii ghidează elevii spre a deveni cetățeni digitali competenți, capabili să învețe independent pe tot parcursul vieții.
