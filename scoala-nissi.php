			<h3 class="h6 fw-semibold text-body mb-3">Punctul de plecare și contextul național</h3>
			<p class="text-body lh-base mb-0">
				Digitalizarea școlii se ancorează în inițiativele naționale timpurii. Programul
				<a href="https://e-estonia.com/what-is-e-education/" target="_blank" rel="noopener noreferrer">Tiger Leap (Tiigrihüpe)</a>,
				lansat în 1996, a asigurat infrastructura de bază (calculatoare și internet) pentru toate școlile din Estonia, inclusiv pentru cele rurale precum Nissi. Lansarea platformei naționale
				<a href="https://ekool.eu" target="_blank" rel="noopener noreferrer">eKool</a>
				în 2002 a marcat trecerea la managementul școlar digital. Nissi Põhikool a adoptat aceste instrumente nu ca opțiuni, ci ca standarde operaționale, beneficiind de un mediu național care a normalizat e-educația.
			</p>
		</div>
