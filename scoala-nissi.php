			<p class="text-body lh-base mb-3">
				În prezent, școala operează într-un mediu „digital-first”. Platforma <span class="fw-bold">eKool</span> funcționează ca sistem nervos central al instituției:
			</p>
			<ul class="list-unstyled text-body lh-base mb-0">
				<li class="mb-3">
					<span class="fw-bold">Obligativitate:</span>
					Conform regulamentului școlar, elevii au obligația de a deține și monitoriza un cont de eKool încă din clasa 1 (cu suportul părinților la vârste mici). Părinții sunt, de asemenea, obligați să mențină conturi active pentru comunicare și monitorizarea notelor.
				</li>
				<li class="mb-3">
					<span class="fw-bold">Administrare dedicată:</span>
					Școala are un rol dedicat de „eKooli administraator” (Reelika Väli), care gestionează drepturile de acces, jurnalele digitale și suportul tehnic pentru părinți și profesori.
				</li>
				<li class="mb-3">
					<span class="fw-bold">Integrare servicii:</span>
					eKool oferă acces Single Sign-On (SSO) către alte resurse educaționale, precum biblioteca digitală <span class="fw-bold">Opiq</span>, facilitând accesul la manuale digitale fără bariere de logare suplimentare.
				</li>
				<li>
					<span class="fw-bold">Procese moderne:</span>
					Școala utilizează
					<a href="https://nissikool.ee/alates-uuest-oppeaastast-laheb-nissi-pohikool-ule-elektroonilisele-ehk-e-opilaspiletile-2/" target="_blank" rel="noopener noreferrer">E-õpilaspilet</a>
					(carnetul de elev electronic), integrat cu sistemul de transport public din Saue și Tallinn. De asemenea, interviurile de dezvoltare (arenguvestlus) și sondajele de satisfacție sunt administrate digital prin eKool.
				</li>
			</ul>
